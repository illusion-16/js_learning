//While loop//

let i = 0
while (i<=10) {
    console.log(`value of i is ${i}`);
    i = i+2
    
}

let myArray = ["Ironman","Thor","Spiderman"]
let arr = 0
while (arr < myArray.length) {
    console.log(`Value is ${myArray[arr]}`);
    arr = arr +1
    
}

//Do-While loop //

let score = 11
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);